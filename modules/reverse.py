async def tele_docs(e):
    try:
        m = e.text.split(None, maxsplit=1)[1]
    except IndexError:
        return await e.reply("Nothing specified!")
    QUERY = ["", "", 0]
    for t in tl:
        for x in tl[t]:
            if x.lower() == m.lower() or x.lower() in m.lower():
                QUERY = [x, t, tl[t].index(x)]
                break
    if QUERY[0] == "":
        return await e.reply("No such method or type found!")
    url = "tl.telethon.dev/" + tlu[QUERY[1]][QUERY[2]]
    await e.reply(str(url))
